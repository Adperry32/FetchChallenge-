package com.example.fetch_codetest.Utilities;

public class Items {
    private int id;
    private int listId;
    private String name;

    public void setId(int id) {
        this.id = id;
    }
    public void setListId(int listId) {
        this.listId = listId;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public int getListId() {
        return listId;
    }

    public String getName() {
        return name;
    }




}
